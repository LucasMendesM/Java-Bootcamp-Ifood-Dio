public class Variaveis {
    
    // Toda varievl em Java deve começar com letra minuscula, e se for nome composto as proximas palavras devem comecar com letra maiuscula (camelCase).

    /*
     * Deve conter apenas letras, _ (underline), $ ou os números de 0 a 9;
     * Deve obrigatoriamente se iniciar por uma letra (preferencialmente), _   ou $, jamais com número;
     * Deve iniciar com uma letra minúscula (boa prática – ver abaixo);
     * Não pode conter espaços;
     * Não podemos usar palavras-chave da linguagem;
     * O nome deve ser único dentro de um escopo.
     */

    // Declaração válida de veriáveis
    int numero$um = 1;
    int numero1 = 1;
    int numero_um = 1;
    int longo = 1;

    // Estrutura
    // Tipo NomeBemDefinido = Atribuicao
    // Exemplo
    int idade = 23;
    double altura = 1.62;
    

}
